  bool has_edge(const Node& a, const Node& b) const {
    // HW0: YOUR CODE HERE
    for (size_type j = 0; j < edgeNum_; ++j) {
      if (edgeVector_[j].node1() == a && edgeVector_[j].node2() == b) {
        return true;
      } else if (edgeVector_[j].node2() == a && edgeVector_[j].node1() == b) {
        return true;
      } else {
        return false;
      }
    }
  }

